type Props = {
    params: Promise<{ service: string }>;
};

export default async function ServicePage({ params }: Props) {
    const { service } = await params;
    const serviceData = servicesData[service as keyof typeof servicesData];

    if (!serviceData) {
        notFound();
    }

    return <ServicePageClient service={serviceData} />;
}

// Генерируем статические параметры для лучшей SEO
export async function generateStaticParams() {
    return Object.keys(servicesData).map((service) => ({
        service: service,
    }));
}

// Метаданные для SEO
export async function generateMetadata({ params }: Props) {
    const { service } = await params;
    const serviceData = servicesData[service as keyof typeof servicesData];

    if (!serviceData) {
        return {
            title: "Услуга не найдена",
            description: "Запрошенная услуга не существует",
        };
    }

    return {
        title: `${serviceData.title} | Строительная компания`,
        description: serviceData.description,
    };
}